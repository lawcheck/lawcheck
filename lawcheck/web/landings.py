async def landing(request: Request, niche: str):
    data = LANDINGS.get(niche)
    if data is None:
        raise HTTPException(status_code=404, detail="landing not found")
    return templates.TemplateResponse(
        request, "landing_niche.html", {"niche": niche, "L": data}
    )
